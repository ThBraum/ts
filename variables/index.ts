//Variáveis

/* var - pode ser alterado e redeclarado - não é gerado dentro de um scopo - pode ser acessado de qualquer lugar do código - não é recomendado
let - pode ser alterado, mas não redeclarado - é gerado dentro de um scopo - só pode ser acessado dentro do scopo - é recomendado
const - não pode ser alterado e nem redeclarado - é gerado dentro de um scopo - só pode ser acessado dentro do scopo - é recomendado */